<p>This limited format is the most popular and recommended for the Final Fantasy TCG.</p>
<ul>
<li>
<p>Requires 4 booster packs per player and 4 players per table. (4 of Opus 17, Opus 18, or 2 of each)</p>
</li>
<li>
<p>Flow of the game:</p>
<ol>
<li>
<p>Each player should open their first booster pack.</p>
</li>
<li>
<p>Each player should pick the card they wish and add it to their ‚Äúcard pool‚Äù.</p>
</li>
<li>
<p>They should then pass the remainder of the pack in the designated direction.</p>
<ul>
<li>Pack Number/Pass cards to the
<ol>
<li>Left</li>
<li>Right</li>
<li>Left</li>
<li>Right</li>
</ol>
</li>
</ul>
</li>
<li>
<p>Each player should then repeat steps 2-3 until there are no cards left in their booster pack.</p>
</li>
<li>
<p>Each player should then open the next pack and repeat steps 2-5 until they have no more packs to open.</p>
</li>
<li>
<p>Players should then build a limited deck using only the cards in their ‚Äúcard pool‚Äù.</p>
</li>
<li>
<p>Players should then play 1 game against each other player in their draft pod of 4 to find an overall winner.</p>
</li>
</ol>
</li>
</ul>
<p>* Draft can also be used in a small scale Swiss tournament with 8 players and 3 rounds.</p>
<p>In addition to the above rules here are more Limited specific rules:</p>
<ul>
<li>
<p>Decks must contain at least 40 cards (40 to 41 cards recommended).</p>
</li>
<li>
<p>Victory is attained by inflicting 6 points of damage instead of the usual 7.</p>
</li>
<li>
<p>Players can have more than three copies of the same card in their deck.</p>
</li>
