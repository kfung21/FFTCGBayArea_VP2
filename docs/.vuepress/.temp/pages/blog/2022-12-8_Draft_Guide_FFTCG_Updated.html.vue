<li>
<p>Remember to draft some Backups of your Elements</p>
</li>
<li>
<p>1CP Evokers/Summoners are actually decent in draft</p>
</li>
</ul>
</li>
<li>
<p>Consider going 3 Elements right from the start</p>
<ul>
<li>
<p>Focus on two based on your feel of the Elements passed your way from left and right</p>
</li>
<li>
<p>Splash the last Element</p>
</li>
</ul>
</li>
<li>
<p>Almost always take the Legendary card from the first pack</p>
<ul>
<li>The Legendary cards are so strong you generally consider building your deck around it</li>
</ul>
</li>
</ol>
