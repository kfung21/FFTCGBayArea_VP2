<p>I had to get the facts straight and it is now confirmed as of this date that playing competitive draft events will only have the following rules that are different from standard rules:</p>
<p><em>4-1. Limited</em>
Special rules are used for tournaments held under the ‚Äúlimited‚Äù rules, such as ‚Äúsealed‚Äù and booster
draft.‚Äù</p>
<p><em>4-1-1. Number of cards in a deck</em> <br>
The number of cards in a ‚Äúlimited‚Äù rules deck is 40 or more. There is no upper limit to the number. In
addition, there is no upper limit to the number of cards of the same serial number that can be
included.</p>
<p><em>4-1-2. Changes to the maximum damage limit</em> <br>
Under the ‚Äúlimited‚Äù rules the game ends once a player has inflicted 6 points of damage on their
opponent.</p>
<p><em>4-1-3. Booster packs used</em> <br>
Under the limited rules, tournament organizers will prepare sealed booster packs and distribute
these to the participating players. Players will not be allowed to use packs or cards that they have
brought themselves.</p>
